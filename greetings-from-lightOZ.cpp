#include<bits/stdc++.h>
using namespace std;

int main() {
  int cases, caseno;
  cin >> cases;
  for(caseno = 1; caseno <= cases; ++caseno ) {
    int a, b;
    cin >> a >> b;
    cout << "Case " << caseno << ": " << a+b << endl;
  }
  return 0;
}

